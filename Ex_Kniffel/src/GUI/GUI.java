    private void tableKniffelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableKniffelMouseClicked

        if (tableKniffel.isEnabled()) {
            if (dtm.getDicetries() <= 3) {
                int row = tableKniffel.getSelectedRow();

                if (ktm.getRow(row).isSelected()) {

                    try {
                        int punkte = bl.chooseSpiel(row, dtm.getDices());
                        if (tableKniffel.getSelectedRow() < 6) {
                            ObereSumme += punkte;

                            tfObereSumme.setText("" + ObereSumme);
                        } else {
                            UntereSumme += punkte;
                            tfUntereSumme.setText("" + UntereSumme);
                        }
                        if (ObereSumme >= 63) {
                            GesamtPunkte = ObereSumme + UntereSumme + ObererBonus;
                            tfObererBonus.setText("" + ObererBonus);
                        } else {
                            GesamtPunkte = ObereSumme + UntereSumme;
                        }
                        tfGesamtPunkte.setText("" + GesamtPunkte);
                        ktm.setPoints(row, punkte);
                        dtm.setDicetries();
                        btWurfeln.setText("w√ºrfeln (3)");
                    } catch (Exception ex) {
                        System.out.println(ex.getMessage());
                    }
                }
            }
        }
        tableKniffel.setEnabled(false);
        tableDices.setCellSelectionEnabled(false);
    }//GEN-LAST:event_tableKniffelMouseClicked

